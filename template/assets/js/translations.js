// Language handler function
function handleLanguage() {
    // Get language from URL parameter, default to Czech
    const urlParams = new URLSearchParams(window.location.search);
    const lang = urlParams.get('lang') || 'cs'; // Changed from 'en' to 'cs'

    // Get translation for current language
    const currentTranslations = translations[lang] || translations.cs; // Changed fallback from translations.en to translations.cs

    // Update content
    function updateContent() {
        // Update navigation links
        const navLinks = document.querySelectorAll('.navbar-collapse .page-scroll');
        navLinks.forEach(link => {
            const section = link.getAttribute('href').replace('#', '');
            if (currentTranslations.navigation[section]) {
                link.textContent = currentTranslations.navigation[section];
            }
        });

        // Update hero section
        document.querySelector('#hero-area h1').textContent = currentTranslations.hero.title;
        document.querySelector('#hero-area .header-content p').textContent = currentTranslations.hero.description;

        // Update main services section
        document.querySelector('#services .section-title-five h2').textContent = currentTranslations.services.title;

        // Update main service items (first three services)
        const serviceItems = document.querySelectorAll('#services .single-services');

        // Taxi Services
        if (serviceItems[0]) {
            serviceItems[0].querySelector('.service-content h4').textContent = currentTranslations.services.taxi.title;
            serviceItems[0].querySelector('.service-content p').textContent = currentTranslations.services.taxi.description;
        }

        // Corporate Transfers
        if (serviceItems[1]) {
            serviceItems[1].querySelector('.service-content h4').textContent = currentTranslations.services.corporate.title;
            serviceItems[1].querySelector('.service-content p').textContent = currentTranslations.services.corporate.description;
        }

        // Airport Transfers
        if (serviceItems[2]) {
            serviceItems[2].querySelector('.service-content h4').textContent = currentTranslations.services.airport.title;
            serviceItems[2].querySelector('.service-content p').textContent = currentTranslations.services.airport.description;
        }

        // Update Taxi PLUS section title
        const taxiPlusTitle = document.querySelector('#services .section-title-five:nth-of-type(2) h2');
        if (taxiPlusTitle) {
            taxiPlusTitle.textContent = currentTranslations.taxiPlus.title;
        }

        // Update Taxi PLUS services
        const taxiPlusServices = [
            { index: 3, key: 'parcelDelivery' },
            { index: 4, key: 'groupTours' },
            { index: 5, key: 'partyTransfers' },
            { index: 6, key: 'fuelDelivery' },
            { index: 7, key: 'flatTire' },
            { index: 8, key: 'emergencyTowing' },
            { index: 9, key: 'jumpStart' },
            { index: 10, key: 'medicineDelivery' }
        ];

        taxiPlusServices.forEach(({ index, key }) => {
            const serviceEl = serviceItems[index];
            if (serviceEl && currentTranslations.taxiPlus[key]) {
                serviceEl.querySelector('.service-content h4').textContent = currentTranslations.taxiPlus[key].title;
                serviceEl.querySelector('.service-content p').textContent = currentTranslations.taxiPlus[key].description;
            }
        });

        // Update pricing section
        document.querySelector('#pricing .section-title-five h2').textContent = currentTranslations.pricing.title;

        // Update pricing cards with proper translations including duration
        const pricingCards = document.querySelectorAll('#pricing .pricing-style-fourteen');

        // Pickup fee
        if (pricingCards[0]) {
            const card = pricingCards[0];
            card.querySelector('.title').textContent = currentTranslations.pricing.pickup.title;
            card.querySelector('.table-head p').textContent = currentTranslations.pricing.pickup.subtitle;
            card.querySelector('.price').innerHTML = `
                <h2 class="amount">
                    <span class="currency">${currentTranslations.pricing.pickup.currency}</span>${currentTranslations.pricing.pickup.amount}<span class="duration">${currentTranslations.pricing.pickup.duration}</span>
                </h2>
            `;
        }

        // Ride
        if (pricingCards[1]) {
            const card = pricingCards[1];
            card.querySelector('.title').textContent = currentTranslations.pricing.ride.title;
            card.querySelector('.table-head p').textContent = currentTranslations.pricing.ride.subtitle;
            card.querySelector('.price').innerHTML = `
                <h2 class="amount">
                    <span class="currency">${currentTranslations.pricing.ride.currency}</span>${currentTranslations.pricing.ride.amount}<span class="duration">${currentTranslations.pricing.ride.duration}</span>
                </h2>
            `;
        }

        // Waiting
        if (pricingCards[2]) {
            const card = pricingCards[2];
            card.querySelector('.title').textContent = currentTranslations.pricing.waiting.title;
            card.querySelector('.table-head p').textContent = currentTranslations.pricing.waiting.subtitle;
            card.querySelector('.price').innerHTML = `
                <h2 class="amount">
                    <span class="currency">${currentTranslations.pricing.waiting.currency}</span>${currentTranslations.pricing.waiting.amount}<span class="duration">${currentTranslations.pricing.waiting.duration}</span>
                </h2>
            `;
        }

        // Update payment info list
        const paymentList = document.querySelector('#pricing .table-list');
        if (paymentList) {
            paymentList.innerHTML = currentTranslations.pricing.paymentInfo
                .map(info => `<li class="text-center"><i class="lni lni-checkmark-circle"></i> ${info}</li>`)
                .join('');
        }

        // Update contact section
        const contactContent = document.querySelector('#contact .contact-content');
        if (contactContent) {
            contactContent.innerHTML = `
                <h4>${currentTranslations.contact.title}</h4>
                <p>${currentTranslations.contact.phone}</p>
                <p>${currentTranslations.contact.email}</p>
                <p>${currentTranslations.contact.availability}</p>
            `;
        }

        // Update call now button
        const callNowButton = document.querySelector('#contact .contact-item a .fs-3');
        if (callNowButton) {
            callNowButton.textContent = currentTranslations.contact.callNow;
        }
    }

    // Call update function when DOM is loaded
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', updateContent);
    } else {
        updateContent();
    }
}

// Initialize language handler
handleLanguage();